using System.Linq;
using System.Numerics;
using Content.Shared.CartridgeLoader.Cartridges;
using Content.Shared.StatusIcon;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Timing;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.GameObjects;
using Robust.Shared.IoC;
using Robust.Shared.Maths;
using Robust.Shared.Prototypes;
using Robust.Shared.Timing;
using Robust.Shared.Utility;

namespace Content.Client.CartridgeLoader.Cartridges;

[GenerateTypedNameReferences]
public sealed partial class MessengerCartridgeUiFragment : BoxContainer
{
    private int _currentChatPartnerId;
    private Dictionary<int, MessengerUserEntry> _users = new();
    private SpriteSystem _spriteSystem = default!;
    private IPrototypeManager _prototypeManager = default!;
    private TimeSpan _lastTypingTime;
    private string _searchText = string.Empty; // DS14

    public MessengerCartridgeUiFragment()
    {
        RobustXamlLoader.Load(this);
        Orientation = LayoutOrientation.Vertical;
        HorizontalExpand = true;
        VerticalExpand = true;

        var entSysMan = IoCManager.Resolve<IEntitySystemManager>();
        _spriteSystem = entSysMan.GetEntitySystem<SpriteSystem>();
        _prototypeManager = IoCManager.Resolve<IPrototypeManager>();

        SendButton.OnPressed += OnSendPressed;
        BackButton.OnPressed += OnBackPressed;
        MessageInput.OnTextEntered += OnMessageInputSubmit;
        MessageInput.OnTextChanged += OnMessageInputTextChanged;
        SearchBar.OnTextChanged += OnSearchTextChanged; // DS14
    }

    // DS14-start
    private void OnSearchTextChanged(LineEdit.LineEditEventArgs args)
    {
        _searchText = args.Text.Trim();
        RebuildUserList();
    }
    // DS14-end

    private void OnMessageInputTextChanged(LineEdit.LineEditEventArgs args)
    {
        var time = IoCManager.Resolve<IGameTiming>().CurTime;
        if (time - _lastTypingTime > TimeSpan.FromSeconds(2))
        {
            _lastTypingTime = time;
            OnTyping?.Invoke();
        }
    }

    public void UpdateState(MessengerStatus status, Dictionary<int, MessengerUserEntry> users, List<MessengerMessageEntry>? messages = null)
    {
        _users = users ?? new Dictionary<int, MessengerUserEntry>();

        ConnectedContainer.Visible = status == MessengerStatus.Connected;
        ConnectionLostContainer.Visible = status == MessengerStatus.ConnectionLost;

        RebuildUserList(); // DS14

        // DS14-start
        if (messages != null && _currentChatPartnerId != 0)
        {
            UpdateMessages(messages);
        }
        // DS14-end
    }

    // DS14-start
    private void RebuildUserList()
    {
        UserListContainer.RemoveAllChildren();

        var filteredUsers = _users.Values
            .Where(MatchesSearch)
            .OrderBy(u => GetNameSortKey(u.Name), StringComparer.CurrentCultureIgnoreCase)
            .ThenBy(u => u.JobTitle, StringComparer.CurrentCultureIgnoreCase)
            .ToList();

        var showingChat = MessageBoxContainer.Visible;
        NoUsers.Visible = filteredUsers.Count == 0 && !showingChat;
        UserListScrollContainer.Visible = filteredUsers.Count > 0 && !showingChat;

        if (filteredUsers.Count == 0)
        {
            NoUsers.Text = string.IsNullOrWhiteSpace(_searchText)
                ? Loc.GetString("messenger-no-users")
                : Loc.GetString("messenger-search-empty");
            return;
        }

        var currentGroup = string.Empty;
        foreach (var user in filteredUsers)
        {
            var group = GetNameGroup(user.Name);
            if (group != currentGroup)
            {
                currentGroup = group;
                UserListContainer.AddChild(MakeGroupHeader(group));
            }

            var button = new Button
            {
                HorizontalExpand = true,
                StyleClasses = { "DS14MenuListRow" },
            };
            var row = new BoxContainer { Orientation = LayoutOrientation.Horizontal, HorizontalExpand = true };

            if (_prototypeManager.TryIndex<JobIconPrototype>(user.JobIconId, out var jobIconProto))
            {
                var icon = new TextureRect
                {
                    Texture = _spriteSystem.Frame0(jobIconProto.Icon),
                    TextureScale = new Vector2(2, 2),
                    VerticalAlignment = VAlignment.Center,
                    Margin = new Thickness(0, 0, 4, 0)
                };
                row.AddChild(icon);
            }

            var labels = new BoxContainer
            {
                Orientation = LayoutOrientation.Vertical,
                HorizontalExpand = true,
            };
            labels.AddChild(new Label
            {
                Text = user.Name,
                ClipText = true,
                HorizontalExpand = true,
                StyleClasses = { "DS14MenuProfileLabel" },
            });
            labels.AddChild(new Label
            {
                Text = user.JobTitle,
                ClipText = true,
                HorizontalExpand = true,
                StyleClasses = { "DS14MenuSubtitle" },
            });
            row.AddChild(labels);

            if (user.UnreadCount > 0)
                row.AddChild(MakeUnreadBadge(user.UnreadCount));

            button.AddChild(row);
            var userId = user.Id;
            button.OnPressed += _ => OnUserSelected(userId);
            UserListContainer.AddChild(button);
        }
    }

    private bool MatchesSearch(MessengerUserEntry user)
    {
        if (string.IsNullOrWhiteSpace(_searchText))
            return true;

        return user.Name.Contains(_searchText, StringComparison.CurrentCultureIgnoreCase)
            || user.JobTitle.Contains(_searchText, StringComparison.CurrentCultureIgnoreCase);
    }

    private static string GetNameSortKey(string name)
    {
        var trimmed = name.Trim();
        return trimmed.Length == 0 ? "#" : trimmed;
    }

    private static string GetNameGroup(string name)
    {
        var trimmed = name.Trim();
        if (trimmed.Length == 0)
            return "#";

        return char.ToUpperInvariant(trimmed[0]).ToString();
    }

    private static Control MakeGroupHeader(string group)
    {
        var panel = new PanelContainer
        {
            HorizontalExpand = true,
            Margin = new Thickness(4, 6, 4, 2),
            StyleClasses = { "DS14MenuListHeader" },
        };

        panel.AddChild(new Label
        {
            Text = group,
            StyleClasses = { "DS14MenuListHeader" },
        });

        return panel;
    }

    private static Control MakeUnreadBadge(int unreadCount)
    {
        var panel = new PanelContainer
        {
            MinWidth = 24,
            VerticalAlignment = VAlignment.Center,
            Margin = new Thickness(6, 0, 0, 0),
            StyleClasses = { "DS14MenuInset" },
        };

        panel.AddChild(new Label
        {
            Text = unreadCount > 9 ? "!" : unreadCount.ToString(),
            Align = Label.AlignMode.Center,
            StyleClasses = { "DS14MenuProfileLabel" },
        });

        return panel;
    }
    // DS14-end

    private void OnUserSelected(int userId)
    {
        _currentChatPartnerId = userId;

        if (_users.TryGetValue(userId, out var user))
        {
            ChatPartnerLabel.Text = user.Name;
        }

        // DS14-start
        SearchBar.Visible = false;
        NoUsers.Visible = false;
        UserListScrollContainer.Visible = false;
        MessageBoxContainer.Visible = true;
        // DS14-end
        OnRequestMessages?.Invoke(userId);
    }

    private void OnBackPressed(BaseButton.ButtonEventArgs args)
    {
        // DS14-start
        MessageBoxContainer.Visible = false;
        SearchBar.Visible = true;
        RebuildUserList();
        // DS14-end
        _currentChatPartnerId = 0;
        OnRequestMessages?.Invoke(0);
    }

    private void OnSendPressed(BaseButton.ButtonEventArgs args)
    {
        SendMessage();
    }

    private void OnMessageInputSubmit(LineEdit.LineEditEventArgs args)
    {
        SendMessage();
    }

    private void SendMessage()
    {
        var text = MessageInput.Text.Trim();
        if (string.IsNullOrEmpty(text) || _currentChatPartnerId == 0)
            return;

        OnSendMessage?.Invoke(_currentChatPartnerId, text);
        MessageInput.Clear();
    }

    private void UpdateMessages(List<MessengerMessageEntry> messages)
    {
        MessageContainer.RemoveAllChildren();

        var filteredMessages = messages.Where(m =>
            m.SenderId == _currentChatPartnerId || m.ReceiverId == _currentChatPartnerId);

        foreach (var msg in filteredMessages.OrderBy(m => m.Timestamp))
        {
            var timeStr = msg.Timestamp.ToString(@"hh\:mm\:ss");
            var color = msg.IsIncoming ? "aqua" : "white";
            // DS14-start
            var safeName = FormattedMessage.EscapeText(msg.SenderName);
            var safeContent = FormattedMessage.EscapeText(msg.Content);
            var safeTime = FormattedMessage.EscapeText(timeStr);
            var richLabel = new RichTextLabel { HorizontalExpand = true };
            richLabel.SetMessage(FormattedMessage.FromMarkup(
                $"[color={color}]{safeTime} {safeName}: {safeContent}[/color]"));
            // DS14-end
            MessageContainer.AddChild(richLabel);
        }
    }

    public event Action<int, string>? OnSendMessage;
    public event Action<int>? OnRequestMessages;
    public event Action? OnTyping;
}
